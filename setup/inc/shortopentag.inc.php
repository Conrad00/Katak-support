<p>Unfortunately for PHP version prior to 5.4 Katak-support requires the PHP directive <b>short_open_tag</b> on. Our apologies for the inconvienence.</p>
<h2>Enabling short_open_tag</h2>
<p>Depending on your server permission level - the directive can be set on in php.ini, .htaccess or httpd.conf.</p>
  