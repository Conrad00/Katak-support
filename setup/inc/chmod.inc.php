
<p>Katak-support installer requires ability to write to the configuration file <strong>include/ktk-config.php</strong>. Please follow the instructions below to give read and write access to the web server.</p>
<h4>Shh/Telnet</h4>
<p>chmod 777 include/ktk-config.php</p>
<h4>FTP</h4>
<p>Using WS_FTP this would be right hand clicking on the file ktk-config.php , selecting chmod, and then giving all permissions to the file.</p>
<h4>Cpanel</h4>
<p>Click on the file, select change permission, and then giving all permissions to the file.</p>
<h4>FileZilla</h4>
<p>Right-click on the file, select file permission, and then giving all permissions to the file.</p>
